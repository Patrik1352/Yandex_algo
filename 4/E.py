n = int(input())

def chat_gpt(n):
    # Решение квадратного уравнения для i, минимизируя ошибку округления
    i = int((-1 + (1 + 8*n)**0.5) / 2)

    # Вычисляем d с использованием формулы
    d = (i**2 + i) // 2
    print(d)
    # Находим h, используя только целочисленные операции
    # Нужно корректировать i и d, чтобы точно определить положение n
    if n > d:
        i += 1
        d = (i**2 + i) // 2

    h = i - (d - n)

    # Правильно форматируем вывод в зависимости от четности строки i
    if i % 2 == 1:
        result = f'{h}/{i+1-h}'
    else:
        result = f'{i+1-h}/{h}'

    return result

print(chat_gpt(7))
